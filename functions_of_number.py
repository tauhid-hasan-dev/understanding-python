# Math functions (functions related to the Number)
import math

pi = 3.5

# Round will round the number to the nearest whole integer
print(round(pi))

# Ceil will round to the highest whole integer
print(math.ceil(pi))

# Floor will round to the lowest whole integer
print(math.floor(pi))

# ABS: it will give us the absolute value of a number, means how far the value from 0;
# if the value of pi is a negative number the output will be a positive value
print(abs(pi))

# pow : will rase a base number to a power
print(pow(pi, 2))

# Square root
print(math.sqrt(64))

